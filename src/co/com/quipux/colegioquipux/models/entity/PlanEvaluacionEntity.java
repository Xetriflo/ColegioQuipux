public class PlanEvaluacionEntity {
     String actividad1;
     String actividad2;
     String actividad3;
     String fecha1;
     String fecha2;
     String fecha3;

    public PlanEvaluacionEntity(String actividad1, String actividad2, String actividad3, String fecha1, String fecha2, String fecha3) {
        this.actividad1 = actividad1;
        this.actividad2 = actividad2;
        this.actividad3 = actividad3;
        this.fecha1 = fecha1;
        this.fecha2 = fecha2;
        this.fecha3 = fecha3;
    }

    public String getActividad1() {
        return actividad1;
    }

    public void setActividad1(String actividad1) {
        this.actividad1 = actividad1;
    }

    public String getActividad2() {
        return actividad2;
    }

    public void setActividad2(String actividad2) {
        this.actividad2 = actividad2;
    }

    public String getActividad3() {
        return actividad3;
    }

    public void setActividad3(String actividad3) {
        this.actividad3 = actividad3;
    }

    public String getFecha1() {
        return fecha1;
    }

    public void setFecha1(String fecha1) {
        this.fecha1 = fecha1;
    }

    public String getFecha2() {
        return fecha2;
    }

    public void setFecha2(String fecha2) {
        this.fecha2 = fecha2;
    }

    public String getFecha3() {
        return fecha3;
    }

    public void setFecha3(String fecha3) {
        this.fecha3 = fecha3;
    }

   
    }

    
    

     

   

    
    

